#include <libs/support/Lambda/la-private.h>


/*--------------------------------------------------------------------------*/
/* la_gcd: computes the gcd of two integers.                                */
/*--------------------------------------------------------------------------*/

int la_gcd(int a,int  b)
  // int a;
  // int b;
{

  int x, y, z;

  if (a>0) x = a;
  else x = (-1) * a;

  if (b>0) y = b;
  else y = (-1) * b;


  /* Euclid algorithm */
  while (x>0)
    {
      z = y % x;
      y = x;
      x = z;
    }

  return (y);
  

}

/*--------------------------------------------------------------------------*/
/* la_gcdV: computes the gcd of an array of integers.                       */
/*--------------------------------------------------------------------------*/

int la_gcdV(la_vect v,int  n)
  // la_vect v;
  // int n;
{

  int i, gcd;

  gcd = v[0]; 
  for (i=1; i<n; i++)
    {
      gcd = la_gcd(gcd, v[i]);

    }

  return(gcd);
}


/*--------------------------------------------------------------------------*/
/* la_lcm: computes the lcm of two integers.                                */
/*--------------------------------------------------------------------------*/

int la_lcm(int a,int  b)
  // int a;
  // int b;
{
  

  return (la_abs(a) * la_abs(b)/la_gcd(a,b));
  

}

/*--------------------------------------------------------------------------*/
/* la_abs: computes the abs of an integer.                                  */
/*--------------------------------------------------------------------------*/

int la_abs(int a)
  // int a;
{
  if (a>0) return(a);
  else return((-1)*a);

}

